#include "../../includes/minishell.h"

char *expand(char *str)
{
    int q[2];
    int i = 0;

    q[0] = 0;
    q[1] = 0;
    while (str && str[i])
    {
        q[0] = (q[0] + (!q[1] && str[i] == "\'")) % 2;
        q[1] = (q[1] + (!q[0] && str[i] == "\"")) % 2;
        if (!q[0] && str[i] == "$" && str[i+1] && !ft_strchr("/~%^{}:; ", str[i+1]))
            return str;
        i++;
    }
    return (str);
}